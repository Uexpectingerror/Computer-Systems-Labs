/* 
 * floatScale2 - Return bit-level equivalent of expression 2*f for
 *   floating point argument f.
 *   Both the argument and result are passed as unsigned int's, but
 *   they are to be interpreted as the bit-level representation of
 *   single-precision floating point values.
 *   When argument is NaN, return argument
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
unsigned floatScale2(unsigned uf) {
// three cases
	//denorm
	//norm
	//nah or zero
	// return !((x >> 23) & 0xFF) && (x & 0x007FFFFF); - if denorm, return 1, so can't be exp any num and frac any num at the same time
	unsigned s = uf >>31;
	unsigned exp = (uf >> 23)&0xff;
	unsigned f = (uf & 0x007FFFFF);
	//nan or infinity
	if (exp == 255) return uf;
	if (exp == 0 &&  (!((uf >> 23) & 0xFF) && (uf & 0x007FFFFF)) ) // denormalized
	{
		return s<<31|exp <<23| (f<<1);
	}
	if (exp == 0 && f == 0) return uf; // if zero, return only sign, i.e. itself
	return s<<31 | (++exp)<<23 | f; //x2 = ++exp //general case, return everything
}
